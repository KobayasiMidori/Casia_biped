function Data_f = notchfilter(Data,Data_pre,Data_f_pre,aa)   
    if abs(aa-2)<0.005 || abs(aa-5)<0.005 %2 and 5   roll
%         num=[0.997964163515322,-1.995911714620733,0.997964163515323];
%         den=[1.000000000000000,-1.995911714620733,0.995928327030645];
