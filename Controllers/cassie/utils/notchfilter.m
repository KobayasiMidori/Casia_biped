%         den=[1.000000000000000,-1.993935534164512,0.993953503031122];
        num =[1.0000,   0,    0];
        den =[1.0000,   0,    0]; 
    elseif abs(aa-1)<0.005 || abs(aa-4)<0.005 %1 and 4   pitch
%         num=[0.997964163515322,-1.995911714620733,0.997964163515323];
%         den=[1.000000000000000,-1.995911714620733,0.995928327030645];
        num =[1.0000,   0,    0];
        den =[1.0000,   0,    0]; 
    else
        num =[1.0000,   0,    0];
        den =[1.0000,   0,    0];        
    end
    uN=Data;
    uN_1 = Data_pre(1);
    uN_2 = Data_pre(2);
    yN_1 = Data_f_pre(1);
    yN_2 = Data_f_pre(2);    
    yN=num*[uN;uN_1;uN_2]-den(2:3)*[yN_1;yN_2];
    Data_f=yN;
end